export const semanaEpidemiologicaUtils = {
  getAnosDisponiveis: () => {
    return Object.keys(calendarios)
      .map(Number)
      .sort((a, b) => a - b)
  },

  getCalendario: (ano) => {
    const calendarioDoAno = calendarios[ano] || []
    return calendarioDoAno.map((s) => ({
      ...s,
      display: `Semana ${String(s.semana).padStart(2, '0')}: ${new Date(s.inicio + 'T00:00:00Z').toLocaleDateString('pt-BR')} a ${new Date(s.fim + 'T00:00:00Z').toLocaleDateString('pt-BR')}`,
    }))
  },

  getSemanaAtual: () => {
    const hoje = new Date()
    const anoAtual = hoje.getFullYear()

    let semanaAtual = (calendarios[anoAtual] || []).find((s) => {
      const inicio = new Date(s.inicio + 'T00:00:00Z')
      const fim = new Date(s.fim + 'T23:59:59Z')
      return hoje >= inicio && hoje <= fim
    })

    if (!semanaAtual) {
      const calendarioAnterior = calendarios[anoAtual - 1] || []
      semanaAtual = calendarioAnterior.find((s) => {
        const inicio = new Date(s.inicio + 'T00:00:00Z')
        const fim = new Date(s.fim + 'T23:59:59Z')
        return hoje >= inicio && hoje <= fim
      })
    }

    if (!semanaAtual) {
      const calendarioProximo = calendarios[anoAtual + 1] || []
      semanaAtual = calendarioProximo.find((s) => {
        const inicio = new Date(s.inicio + 'T00:00:00Z')
        const fim = new Date(s.fim + 'T23:59:59Z')
        return hoje >= inicio && hoje <= fim
      })
    }

    return semanaAtual
  },
}
